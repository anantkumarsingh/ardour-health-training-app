package com.app.atl_test_app_1.carddata;

public class ActivityCardData {



    // Fields for Type One card data

    private String mHospitalName;
    private String mHospitalArea;
    private Integer mHospitalImage;


    public ActivityCardData(String hospitalName, String hospitalArea, Integer hospitalImage) {

        mHospitalName = hospitalName;
        mHospitalArea = hospitalArea;
        mHospitalImage = hospitalImage;
    }


    public String getHospitalName() {
        return mHospitalName;
    }

    public void setHospitalName(String hospitalName) {
        mHospitalName = hospitalName;
    }

    public String getHospitalArea() {
        return mHospitalArea;
    }

    public void setHospitalArea(String hospitalArea) {
        mHospitalArea = hospitalArea;
    }

    public Integer getmHospitalImage() {
        return mHospitalImage;
    }

    public void setmHospitalImage(Integer hospitalImage) {
        mHospitalImage = hospitalImage;
    }























}

